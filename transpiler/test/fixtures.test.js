test("fixture examples tokenize into the expected token sequences", async () => {
  const names = (await readdir(fixturesRoot)).sort();

  assert.deepEqual(names, [
    "arithmetic-reactive.flow",
    "check-cases.flow",
    "collection-pipeline.flow",
    "collections.flow",
    "comparisons.flow",
    "first-test.flow",
    "formatting-soft-words.flow",
    "list-loops.flow",
    "logic-strings.flow",
    "repeat-while-until.flow",
    "when-conditions.flow"
  ]);

  for (const name of names) {
    const filePath = join(fixturesRoot, name);
    const tokens = await lexFile(filePath);
    const kinds = normalizeKinds(tokens.map((token) => token.kind));

    assert.deepEqual(kinds, normalizeKinds(expectations[name]), `Unexpected token kinds for ${name}`);
  }
});

function normalizeKinds(kinds) {
  if (kinds.length >= 2 && kinds.at(-1) === TOKEN_KINDS.EOF && kinds.at(-2) === TOKEN_KINDS.NEWLINE) {
    return [...kinds.slice(0, -2), TOKEN_KINDS.EOF];
  }

  return kinds;
}
